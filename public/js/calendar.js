generateCalendar = (month, year) => {

    let calendar_days = calendar.querySelector('.calendar-days')
    let calendar_header_year = calendar.querySelector('#year')

    let days_of_month = [31, getFebDays(year), 31, 30, 31, 30, 31, 31, 30, 31, 30, 31]
    var days = ['Minggu', 'Senin', 'Selasa', 'Rabu', 'Kamis', 'Jumat', 'Sabtu'];

    calendar_days.innerHTML = ''

    let currDate = new Date()
    if (!month) month = currDate.getMonth()
    if (!year) year = currDate.getFullYear()

    let agendaDate = new Date(year, month, 11)
    if (!month) month = currDate.getMonth()
    if (!year) year = currDate.getFullYear()

    let curr_month = `${month_names[month]}`
    month_picker.innerHTML = curr_month
    calendar_header_year.innerHTML = year

    // get first day of month

    let first_day = new Date(year, month, 1)

    for (let i = 0; i <= days_of_month[month] + first_day.getDay() - 1; i++) {
        let day = document.createElement('div')
        if (i >= first_day.getDay()) {
            day.classList.add('calendar-day-hover')
            day.innerHTML = i - first_day.getDay() + 1
            day.innerHTML += `<span></span>
                            <span></span>
                            <span></span>
                            <span></span>`
            if (i - first_day.getDay() + 1 === currDate.getDate() && year === currDate.getFullYear() && month === currDate.getMonth()) {
                day.classList.add('curr-date')
            }
            if (i - first_day.getDay() + 1 === agendaDate.getDate() && year === agendaDate.getFullYear() && month === agendaDate.getMonth()) {
                let agenda = document.createElement('a')
                agenda.setAttribute('data-bs-target','#agendaPopUp')
                agenda.setAttribute('data-bs-toggle','modal')
                agenda.classList.add('agenda')
                var strAgenda = days[agendaDate.getDay()] + ", "+ month_names[agendaDate.getMonth()] + " " + agendaDate.getFullYear();
                document.getElementById('agendaDate').innerHTML= strAgenda;
                day.appendChild(agenda)
            }
        }
        calendar_days.appendChild(day)
    }
}

